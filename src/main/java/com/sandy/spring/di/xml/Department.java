package com.sandy.spring.di.xml;

public class Department {

    private int id;
    private String name;

    public Department() {
        this.id = 2000;
        this.name = "Software";
    }

    public int getId() {
        return id;
    }

    public void setId(final int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Department{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
